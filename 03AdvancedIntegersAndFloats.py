# Integers are whole numbers from negative infinity to positive infinity without decimals.
# Floats are starting from negative infinity to positive infinity but with decimals after each Integers.
# Example of Integer are like: 1,2,-3,-4. Example of Float number are like: 1.05, -2.4567.
print(3 / 2)  # Division
print(3 // 2)  # Floor Division (tells how many time 2 is successfully divisible to 3 which is 1)
print(3 + 2)  # Addition
print(3 - 2)  # Subtraction
print(3 ** 2)  # Exponents, for example: 3 ** 2 = 9
print(3 % 2)  # Modulus, also known as Remainder (tells you how much will be left after successfully dividing 3 in 2
# which is 1)
print(abs(-3))  # Prints Absolute value of a number which is always positive.
print(round(3.75, 1))  # Rounding the integer 1 number after the decimals "If it is bigger than 5 then it will choose
# up, else down'
3 == 2  # Checks if 3 is equal to 2
3 != 2  # Check if 3 is NOT equal to 2
3 > 2  # Checks if 3 is bigger than 2
3 < 2  # Checks if 3 is smaller than 2
3 >= 2  # Checks if 3 is equal OR bigger than 2
3 <= 2  # Checks if 3 is equal OR smaller than 2

# Converting an string number into actual Integers is Possible.
# Examples below:
num_1 = '100'  # String number (you can't use them in operations like + - * /)
num_2 = '200'  # ^
num_1 = int(num_1)  # Converting String numbers into Integer
num_2 = int(num_2)  # Converting string numbers into Integer

